package Chap_02;

public class _03_Operator3 {
    public static void main(String[] args) {
        System.out.println(5 > 3); // 5는 3보다 크다 (참)
        System.out.println(5 >= 5); // 5는 5보다 크거나 같다 (참)
        System.out.println(5 < 3); // 5는 3보다 작다 (거짓)
        System.out.println(5 == 5); // 5는 5와 같다 (참)
        System.out.println(5 != 5); // 5는 5와 같지 않다 (거짓)

    }
    //비교 연산자

}
